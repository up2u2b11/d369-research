def digit_root(n: int) -> int:
    """الجذر الرقمي — تكرار جمع الأرقام حتى رقم واحد (1-9)"""
    n = abs(int(n))
    if n == 0:
        return 0
    while n >= 10:
        n = sum(int(d) for d in str(n))
    return n

def word_value(text: str, system: dict) -> int:
    """قيمة نص بنظام حساب محدد"""
    return sum(system.get(ch, 0) for ch in text)

def text_digit_root(text: str, system: dict) -> int:
    """جذر رقمي لنص كامل بنظام محدد"""
    return digit_root(word_value(text, system))
